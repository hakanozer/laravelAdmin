                <script type="text/javascript">

                    //<![CDATA[

                    jQuery(document).ready(function ($) {

                        ;

                        (function (element) {

                            var $el = $(element);



                            function runSlider() {

                                $('.slider', $el).responsiver({

                                    interval: 0,

                                    speed: 800,

                                    start: 0,

                                    step: 1,

                                    circular: true,

                                    preload: true,

//                        fx: 'fade',

                                    pause: 'hover',

                                    control: {

                                        prev: '#sp_manu_slider .control-button a[class="preview"]',

                                        next: '#sp_manu_slider .control-button a[class="next"]'

                                    },

                                    getColumns: function (_element) {

                                        var match = $(_element).attr('class').match(/cols-(\d+)/);

                                        if (match[1]) {

                                            var column = parseInt(match[1]);

                                        } else {

                                            var column = 1;

                                        }

                                        if (!column) column = 1;

                                        return column;

                                    }

                                });

                            }



                            var _timer = 0;

                            $(window).load(function () {

                                if (_timer) clearTimeout(_timer);

                                _timer = setTimeout(function () {

                                    $el.removeClass('sp-preload');

                                    $('.sp-loading', $el).remove();

                                    runSlider();

                                }, 1000);

                            });



                            $('.slider', $el).touchSwipeLeft(function () {

                                        $('.slider', $el).responsiver('next');

                                    }

                            );

                            $('.slider', $el).touchSwipeRight(function () {

                                        $('.slider', $el).responsiver('prev');

                                    }

                            );



                        })('#sp_manu_slider')



                    });

